import sys

morse_code = {
    "A": ".- ", "B": "-... ", "C": "-.-. ", "D": "-.. ", "E": ". ",
    "F": "..-. ", "N": "-. ", "O": "--- ", "P": ".--. ", "Q": "--.- ",
    "R": ".-. ", "S": "... ", "G": "--. ", "T": "- ", "H": ".... ",
    "U": "..- ", "I": ".. ", "V": "...- ", "J": ".--- ", "W": ".-- ",
    "K": "-.- ", "X": "-..- ", "L": ".-.. ", "Y": "-.-- ", "M": "-- ",
    "Z": "--.. ", " ": "/ ", "1": "·---- ", "2": "··--- ", "3": "···-- ",
    "4": "····- ", "5": "····· ", "6": "-···· ", "7": "--··· ",
    "8": "---·· ", "9": "----· ", "0": "----- ",
}

# Validation check
for str in sys.argv[1:]:
    if '/' in str:
        print('ERROR')
        sys.exit()

# Str to Morse
result = ''
for str in sys.argv[1:]:
    str_result = ''
    for char in str:
        str_result += morse_code[char]
    if str != sys.argv[-1:]:
        str_result += '/'
    result += str_result

print(result)
sys.exit()
